                                            <h2 class="post-title mb-20">
                                                <a href="blog-details.html">Livestock Health and Disease Control</a></h2>
                                            <p class="post-exerpt font-medium text-muted mb-30">The overall aim of the Livestock Health & Disease Control scheme is to improve the animal health sector by way of implementation of prophylactic vaccination programmes against various diseases of livestock and poultry, capacity building, disease surveillance and strengthening of veterinary infrastructure. It is envisaged that implementation of the scheme will ultimately lead to prevention & control, subsequently eradicating the diseases, increased access to veterinary services, higher productivity from animals, boosting up of trade in livestock and poultry, in livestock and poultry products and improving socio- economic status of livestock and poultry farmers. The funding pattern is 100% central assistance for the CADCP and the non-recurring components of ESVHD, and 60:40 between Central and State for the other components as well as for ASCAD, with 90:10 for hilly and NE States and 100% for UTs.

